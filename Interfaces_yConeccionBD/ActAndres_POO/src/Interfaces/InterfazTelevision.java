    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:

        try {
            Television tv = new Television();

            tv.setIdProducto(Integer.parseInt(jTF_ID.getText()));
            tv.setNombreProducto(jTextField2.getText());
            tv.setMarcaProducto(jTextField3.getText());
            tv.setPrecioProducto(Double.parseDouble(jTextField5.getText()));
            tv.setCantidadExistencia(Integer.parseInt(jTextField6.getText()));
            tv.setDescuentoProducto(Double.parseDouble(jTextField7.getText()));
            tv.setPulgadasTelevision(Double.parseDouble(jTextField8.getText()));
            tv.setTipoPantalla(jTextField9.getText());
            tv.setNumPuertosHDMI(Integer.parseInt(jTextField10.getText()));
            tv.setNumPuertosUSB(Integer.parseInt(jTextField11.getText()));

            boolean exito = OAD_Television.modificarTelevisor(tv);


        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Verifica que todos los campos est√©n llenos y con datos v√°lidos.");
        }
        
       
    }//GEN-LAST:event_jButton6ActionPerformed

