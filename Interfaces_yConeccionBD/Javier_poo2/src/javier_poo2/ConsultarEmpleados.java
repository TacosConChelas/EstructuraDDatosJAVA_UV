    public void cargarEmpleadosEnTabla() {
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
    modelo.setRowCount(0); // Limpia la tabla antes de cargar

    EmpleadoDAO dao = new EmpleadoDAO();
    List<Empleado> lista = dao.obtenerTodosLosEmpleados();

    for (Empleado e : lista) {
        Object[] fila = new Object[] {
            e.getIdEmpleado(),
            e.getNombre(),
            e.getFechaNacimiento(),
            e.getGenero(),
            e.getCorreo(),
            e.getSalario(),
            e.getPuesto(),
            e.getFunciones(),
            e.getFechaContratacion(),
            e.getGradoEstudios()
        };
        modelo.addRow(fila);
    }
}


