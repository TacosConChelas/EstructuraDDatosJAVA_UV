        jTextField6.setText(String.valueOf(invF));
        
        costoPT=iipenproceso+costoMaterialDirecto+manoObraAplicada+costoCIF-ProduccionProceso;
        
        jTextField49.setText(String.valueOf(costoPT));
        
        Double costoAD = Double.parseDouble(jTextField50.getText());
        
        jTextField51.setText(String.valueOf(costoPT));
        
        Double invFAT = Double.parseDouble(jTextField52.getText());
        
        costoPV=iiArticulosT+costoPT-invFAT;
        jTextField71.setText(String.valueOf(costoPV));
        
