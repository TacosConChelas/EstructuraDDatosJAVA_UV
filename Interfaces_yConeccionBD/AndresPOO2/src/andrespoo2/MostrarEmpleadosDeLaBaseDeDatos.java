    public void traerDatos() {
       DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
       modelo.setRowCount(0); // Limpia la tabla antes de cargar

       Empleados_BaseDatos baseDatos = new Empleados_BaseDatos();
       List<Empleados> lista = baseDatos.listarEmpleados();

       for (Empleados emp : lista) {
           Object[] fila = new Object[] { emp.getCodigoEmpleado(), emp.getNombres(), emp.getNacimiento(), emp.getSexo(), emp.getEmail(), emp.getSueldo(), emp.getCargo(),
               emp.getResponsabilidades(), emp.getIngreso(), emp.getNivelAcademico()           };
           modelo.addRow(fila);
       }    
    }


