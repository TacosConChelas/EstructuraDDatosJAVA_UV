    private void validarCredenciales() {
        String nombreUsuario = jTNombre.getText();
        String contrasenaUsuario = jTContraseña.getText();

        String sql = "select * from usuarios where idUsuarios = " + nombreUsuario + ";";
        try{
            con = conect.getConnection();
            st = con.createStatement();
            rs= st.executeQuery(sql);
            System.out.println("Se conectó correctamente a DB pp");
            
            //Evaluacion si contiene alguna columna 
            if(rs.next()){
                String pwdFromBD = rs.getString("Contraseña");
                
                if(contrasenaUsuario.equals(pwdFromBD)){
                   VentaPrincipal ventP = new VentaPrincipal();
                   ventP.setVisible(true);
                   
                } else {    JOptionPane.showMessageDialog(null, "Contraseña incrrecta");        }
            } else {    JOptionPane.showMessageDialog(null, "Lo sentimos pero este usuario no existe");     }
        }   catch(SQLException e){      System.out.println(" El error es " + e);
        }  finally {
            try {
                if (rs != null) rs.close();
                if (st != null) st.close();
                if (con != null) con.close();
            } catch (SQLException e) {      System.out.println("Error al cerrar la conexión " + e);     }
        }
    }
        
